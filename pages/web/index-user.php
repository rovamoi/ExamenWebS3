					}
				}
			};

			xhr.open("POST", "../traitement/login.php");
			xhr.send(form);


			function createXHR() {
                var xhr;
                try {
                    xhr = new XMLHttpRequest();
                } catch (e) {
                    alert("Une erreur s'est produite lors de la création de l'objet XMLHttpRequest.");
                    return null;
                }
                return xhr;
            }
